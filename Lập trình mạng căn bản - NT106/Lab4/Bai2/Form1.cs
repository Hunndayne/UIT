        private async void Search_Click(object sender, EventArgs e)
        {
            ClearBrowser();
            WebBrowser webBrowser = new WebBrowser
            {

                Location = new Point(50, 250),
            };
            SetWebBrowserSize(webBrowser);

            richTextBox1.Text = "Loading...";

            string urlInput = URL.Text.Trim();

            // Automatically add "http://" if no scheme is provided.
            if (!urlInput.StartsWith("http://") && !urlInput.StartsWith("https://"))
            {
                urlInput = "http://" + urlInput;
            }

            // Validate URL
            if (!Uri.TryCreate(urlInput, UriKind.Absolute, out Uri uriResult) ||
                (uriResult.Scheme != Uri.UriSchemeHttp && uriResult.Scheme != Uri.UriSchemeHttps))
            {
                richTextBox1.Text = "Please enter a valid URL.";
                return;
            }

            // Get the selected User-Agent string from the ComboBox
            string userAgent = comboBox1.Text;

            // Fetch HTML content asynchronously using HttpClient
            string htmlContent = await GetHtmlContentAsync(urlInput, userAgent);
            richTextBox1.Text = htmlContent;

            // Set the WebBrowser's HTML content and add it to the form
            webBrowser.DocumentText = htmlContent;
            this.Controls.Add(webBrowser);
            webBrowser.ScriptErrorsSuppressed = true;

        }

        private async Task<string> GetHtmlContentAsync(string url, string userAgent)
        {
            try
            {
                using (HttpClientHandler handler = new HttpClientHandler())
                {
                    using (HttpClient client = new HttpClient(handler))
                    {
                        client.DefaultRequestHeaders.Add("User-Agent", userAgent);
                        return await client.GetStringAsync(url);
                    }
                }
            }
            catch (Exception ex)
            {
                return $"Error: {ex.Message}";
            }
        }

        private void SetWebBrowserSize(WebBrowser webBrowser)
        {
            string selectedSize = comboBox2.SelectedItem.ToString();
            if (selectedSize == "Desktop (1920x1080)")
            {
                webBrowser.Size = new Size(1920, 1080);
            }
            else if (selectedSize == "Tablet (800x1280)")
            {
                webBrowser.Size = new Size(800, 1280);
            }
            else if (selectedSize == "Mobile (360x640)")
            {
                webBrowser.Size = new Size(360, 640);
            }
        }

        private void Clear_Click(object sender, EventArgs e)
        {
            URL.Clear();
            richTextBox1.Clear();
            ClearBrowser();
        }
        private void ClearBrowser()
        {
            foreach (Control control in this.Controls)
            {
                if (control is WebBrowser)
                {
                    this.Controls.Remove(control);
                    control.Dispose(); // Free up resources
                }
            }
        }
